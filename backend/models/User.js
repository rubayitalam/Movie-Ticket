import mongoose from 'mongoose';

const useSchema = new mongoose.Schema({
    _id: {
        type: String,
        required: true
    },
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    image: {
        type: String
    }
});
const User = mongoose.model('User', useSchema);
export default User;
